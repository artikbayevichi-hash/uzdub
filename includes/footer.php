(function() {
    var container = document.getElementById('floatingSocials');
    if (!container) return;
    var icons = container.querySelectorAll('.float-soc');
    var size = 48;
    var paused = {};

    function getBounds() {
        return { w: window.innerWidth, h: window.innerHeight };
    }

    var balls = [];
    icons.forEach(function(icon, i) {
        var b = getBounds();
        var vx = (Math.random() - 0.5) * 1.4;
        var vy = (Math.random() - 0.5) * 1.4;
        if (Math.abs(vx) < 0.35) vx = 0.35 * (Math.random() > 0.5 ? 1 : -1);
        if (Math.abs(vy) < 0.35) vy = 0.35 * (Math.random() > 0.5 ? 1 : -1);

        balls.push({
            el: icon,
            x: Math.random() * (b.w - size),
            y: Math.random() * (b.h - size),
            vx: vx,
            vy: vy,
            glow: 0
        });

        icon.addEventListener('mouseenter', function() { paused[i] = true; balls[i].glow = 1; });
        icon.addEventListener('mouseleave', function() { paused[i] = false; balls[i].glow = 0; });
    });

    function tick() {
        var b = getBounds();
        balls.forEach(function(ball, i) {
            if (paused[i]) {
                ball.el.style.left = ball.x + 'px';
                ball.el.style.top = ball.y + 'px';
                return;
            }

            ball.x += ball.vx;
            ball.y += ball.vy;

            if (ball.x <= 0) { ball.x = 0; ball.vx = Math.abs(ball.vx); }
            if (ball.x >= b.w - size) { ball.x = b.w - size; ball.vx = -Math.abs(ball.vx); }
            if (ball.y <= 0) { ball.y = 0; ball.vy = Math.abs(ball.vy); }
            if (ball.y >= b.h - size) { ball.y = b.h - size; ball.vy = -Math.abs(ball.vy); }

            ball.el.style.left = ball.x + 'px';
            ball.el.style.top = ball.y + 'px';
        });
        requestAnimationFrame(tick);
    }
    tick();

    window.addEventListener('resize', function() {
        var b = getBounds();
        balls.forEach(function(ball) {
            if (ball.x > b.w - size) ball.x = b.w - size;
            if (ball.y > b.h - size) ball.y = b.h - size;
        });
    });
})();
